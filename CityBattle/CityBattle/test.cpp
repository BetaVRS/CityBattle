IMAGE wall, stone, empty, background;

int main()
{
	initgraph(WIN_WIDTH, WIN_HEIGHT,SHOWCONSOLE);

	TestTank player(&map);
	BeginBatchDraw();	//开始批量绘图
	IMAGE canvas(CANVAS_WIDTH, CANVAS_HEIGT);
	loadimg();
	HDC win_hdc = GetImageHDC();
	HDC canvas_hdc = GetImageHDC(&canvas);
	
	player.ShowBox();
	//测试主循环
	while (true)
	{
		player.PlayerControl();
		RefreshMap(&map, &canvas);
		player.DrawTank(canvas_hdc);
		StretchBlt(win_hdc, 0, 0, WIN_WIDTH, WIN_HEIGHT, canvas_hdc, 0, 0, CANVAS_WIDTH, CANVAS_HEIGT, SRCCOPY);
		FlushBatchDraw();

		Sleep(1);
	}

	EndBatchDraw();
	closegraph();
	system("pause");//getchar();
	return 0;
}

void loadimg()
{
		loadimage(&background, "./res/big/bg_black.gif");
		loadimage(&stone, "./res/big/stone.gif");
		loadimage(&wall, "./res/big/wall.gif");
}

void RefreshMap(MarkBox *map, IMAGE *canvas)
{
	SetWorkingImage(canvas);
	putimage(0,0,&background);
	for (int i = 0; i < 26; i++)
	{
		for (int j = 0; j < 26; j++)
		{
			switch (map->box_8[i][j])
			{
			case _WALL:putimage(8 * j, 8 * i, &wall); break;
			case _STONE:putimage(8 * j, 8 * i, &stone); break;
			default:break;
			}
			//Sleep(1); 
		}
	}
	SetWorkingImage();
}
